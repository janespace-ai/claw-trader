#!/usr/bin/env node
// Generates `src/types/api.d.ts` from `api/openapi.yaml` using
// `openapi-typescript`. Emits a banner comment + pinned formatting so
// CI diff checks are stable.

import { execSync } from 'node:child_process';
import { writeFileSync, readFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const SPEC = join(__dirname, '..', '..', 'api', 'openapi.yaml');
const OUT = join(__dirname, '..', 'src', 'types', 'api.d.ts');

const BANNER = `// DO NOT EDIT — this file is generated by scripts/gen-api-types.mjs
// Regenerate via \`pnpm api:types\` (or \`npm run api:types\`) after any
// change to api/openapi.yaml. CI fails if this file is out of sync with
// the spec.
`;

// Use npx so the local openapi-typescript binary is picked up.
const raw = execSync(`npx --yes openapi-typescript "${SPEC}"`, {
  encoding: 'utf8',
  maxBuffer: 10 * 1024 * 1024,
});

writeFileSync(OUT, BANNER + '\n' + raw);

// eslint-disable-next-line no-console
console.log('✓ wrote', OUT, `(${raw.length} bytes)`);
