#!/usr/bin/env node
// Generates `src/mocks/handlers.ts` from `api/openapi.yaml` + `api/examples/`.
// One handler per operationId. Supports three profiles via
// `CLAW_MOCK_PROFILE` env at runtime: happy (default) / slow / chaos.

import { readFileSync, readdirSync, writeFileSync } from 'node:fs';
import { dirname, join, basename } from 'node:path';
import { fileURLToPath } from 'node:url';
import yaml from 'js-yaml';

const __dirname = dirname(fileURLToPath(import.meta.url));
const SPEC = join(__dirname, '..', '..', 'api', 'openapi.yaml');
const EXAMPLES = join(__dirname, '..', '..', 'api', 'examples');
const OUT = join(__dirname, '..', 'src', 'mocks', 'handlers.ts');

const spec = yaml.load(readFileSync(SPEC, 'utf8'));

const ops = []; // { operationId, method, pathPattern, exampleRelPath }
for (const [pathPattern, methods] of Object.entries(spec.paths ?? {})) {
  for (const [method, op] of Object.entries(methods)) {
    if (!['get', 'post', 'put', 'patch', 'delete'].includes(method)) continue;
    if (!op.operationId) continue;
    const exampleFile = `${op.operationId}.json`;
    try {
      readFileSync(join(EXAMPLES, exampleFile), 'utf8'); // exists?
    } catch {
      // No example — skip; api-lint will flag it.
      continue;
    }
    ops.push({
      operationId: op.operationId,
      method,
      pathPattern,
      exampleFile,
    });
  }
}

// Convert OpenAPI {param} to MSW :param syntax, and prefix a host
// wildcard (`*`) so the handler matches regardless of origin. In
// Electron renderer `fetch('/api/...')` resolves against the current
// page origin; in Node tests we always use `http://localhost/...`.
// Host wildcard covers both.
const mswPath = (p) => '*' + p.replace(/\{([^}]+)\}/g, ':$1');

const banner = `// DO NOT EDIT — generated by scripts/gen-msw-handlers.mjs
// Regenerate via \`pnpm api:mocks\` after editing api/openapi.yaml or
// api/examples/. CI fails if this file drifts from the spec.

import { http, HttpResponse, delay } from 'msw';
import type { RequestHandler } from 'msw';
`;

const header = `
const PROFILE =
  (typeof import.meta !== 'undefined' &&
    (import.meta as unknown as { env?: Record<string, string> }).env
      ?.CLAW_MOCK_PROFILE) ||
  (typeof process !== 'undefined' && process.env?.CLAW_MOCK_PROFILE) ||
  'happy';

const ERROR_CODES = [
  'INVALID_INTERVAL',
  'SYMBOL_NOT_FOUND',
  'INVALID_RANGE',
  'COMPLIANCE_FAILED',
  'SANDBOX_ERROR',
  'SANDBOX_TIMEOUT',
  'DATA_UNAVAILABLE',
  'UPSTREAM_UNREACHABLE',
  'INTERNAL_ERROR',
] as const;

function maybeError() {
  if (PROFILE !== 'chaos') return null;
  if (Math.random() >= 0.15) return null;
  const code = ERROR_CODES[Math.floor(Math.random() * ERROR_CODES.length)];
  const status = code === 'INTERNAL_ERROR' || code === 'UPSTREAM_UNREACHABLE' ? 500 : 400;
  return HttpResponse.json(
    { error: { code, message: 'simulated chaos', details: {} } },
    { status },
  );
}

async function maybeDelay() {
  if (PROFILE === 'slow') {
    await delay(500 + Math.floor(Math.random() * 1000));
  }
}
`;

const handlerLines = ops.map(({ operationId, method, pathPattern, exampleFile }) => {
  const example = JSON.parse(readFileSync(join(EXAMPLES, exampleFile), 'utf8'));
  const payload = JSON.stringify(example);
  return `
  // ${operationId}
  http.${method}('${mswPath(pathPattern)}', async () => {
    await maybeDelay();
    const chaos = maybeError();
    if (chaos) return chaos;
    return HttpResponse.json(${payload});
  }),`;
});

const content = `${banner}${header}

export const handlers: RequestHandler[] = [${handlerLines.join('')}
];

export const operationIds = ${JSON.stringify(ops.map((o) => o.operationId))} as const;
`;

writeFileSync(OUT, content);
// eslint-disable-next-line no-console
console.log('✓ wrote', OUT, `with ${ops.length} handlers`);
